def preparar_par(S, EEG):
    """
    S: (features_lag, T)  -> nos quedamos solo con la señal base
    EEG: (T, canales)
    """

    # Take only the first row (base stimulus).
    S = S[0, :]          # (T,)
    S = S[:, np.newaxis]  # (T, 1)

    T = min(S.shape[0], EEG.shape[0])
    S = S[:T, :]
    EEG = EEG[:T, :]

    return S, EEG

#Function to split data into training and test sets
def split_train_test(stim_list, eeg_list, test_ratio=0.2, seed=42):
    n = len(stim_list)
    idx = np.arange(n)
    rng = np.random.default_rng(seed)
    rng.shuffle(idx)

    split = int((1 - test_ratio) * n)
    train_idx = idx[:split]
    test_idx  = idx[split:]

    stim_train = [stim_list[i] for i in train_idx]
    eeg_train  = [eeg_list[i] for i in train_idx]
    stim_test  = [stim_list[i] for i in test_idx]
    eeg_test   = [eeg_list[i] for i in test_idx]

    return stim_train, eeg_train, stim_test, eeg_test

