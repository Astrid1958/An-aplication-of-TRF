#Choose the best lambda / Cross-validation
def seleccionar_lambda(S_train, y_train,S_val, y_val,fs, tmin, tmax, lambdas):

    trf = TRF(direction=1)

    scores = []

    for lam in lambdas:
        #Train the model using this lambda_i
        model = trf.train(S_train, y_train, fs, tmin, tmax, lam)

        # Predict on the same data
        y_pred_list = trf.predict(S_val, model)

        y_true_all = []
        y_pred_all = []

        for yt, yp in zip(y_train, y_pred_list):
            yt = np.asarray(yt).ravel()
            yp = np.asarray(yp).ravel()
            T = min(len(yt), len(yp))
            if T < 2:
                continue
            y_true_all.append(yt[:T])
            y_pred_all.append(yp[:T])

        if len(y_true_all) == 0:
            scores.append(np.nan)
        else:
            y_true = np.concatenate(y_true_all)
            y_pred = np.concatenate(y_pred_all)
            r, _ = pearsonr(y_true, y_pred)
            scores.append(r)

    # best lambda
    idx_best = np.nanargmax(scores)
    best_lambda = lambdas[idx_best]

    return best_lambda, scores

"""Formalmente, para cada canal $c$, el modelo estima la función de respuesta temporal $w^c(\tau)$ resolviendo el siguiente problema de optimización:

\begin{equation}
\hat{w}^{\,c}(\tau)
=
\arg\min_{w}
\left\|
y_c(t)
-
\sum_{k}
\sum_{\tau}
S_k(t-\tau)\, w_{k,c}(\tau)
\right\|^2
+
\lambda \|w\|^2
\end{equation}

donde:
    - $y_c(t)$ es la señal EEG observada en el canal $c$.
    - $S_k(t)$ representa el predictor $k$ del estímulo (por ejemplo, vocales o consonantes).
    - $w_{k,c}(\tau)$ es la TRF asociada al predictor $k$, canal $c$ y desfase $\tau$.
    - $\lambda$ es el parámetro de regularización que controla el compromiso entre ajuste y complejidad del modelo.

TRF directa (modelo forward):

\begin{equation}
\hat{Y}(t,c)
=
\sum_{k=1}^{K}
\sum_{\tau=0}^{\tau_{\max}}
S_k(t-\tau)\, w_{k,c}(\tau)
\end{equation}

donde:
    - $S_k(t)$: matriz de estímulo (fonemas vocales y consonantes con desfases temporales).
    - $Y(t,c)$: señal EEG en el tiempo $t$ y canal $c$.
    - $w_{k,c}(\tau)$: función de respuesta temporal (TRF), que modela la contribución del predictor $k$ al canal $c$ con desfase $\tau$.
    - $K$: cantidad de muestras (en este caso, vocales y consonantes).
    - $\tau$: desfase temporal (lags), con $0 \le \tau \le \tau_{\max}$.
"""

